#ifndef ATMEGA32_H_
#define ATMEGA32_H_







//---------------------------------------------------------------------------------------------------
//	Include
//---------------------------------------------------------------------------------------------------
#include "stdio.h"
#include "stdlib.h"
#include "stdint.h"



//===================================================================================================================



//---------------------------------------------------------------------------------------------------
//	Base Addresses for Memories
//---------------------------------------------------------------------------------------------------
#define FLASH_MEMORY_BASE 							0x3FFFU
#define SYSTEM_MEMORY_BASE 							0x0000U
#define SRAM_MEMORY_BASE 							0x0060U
#define PERIPHERALS_BASE 							0x0020U



//---------------------------------------------------------------------------------------------------
//	Base Addresses of Peripherals
//---------------------------------------------------------------------------------------------------

//	IO *******
//	Port [A]
#define GPIOA_BASE 							0x39U
//	Port [B]
#define GPIOB_BASE 							0x36U
//	Port [C]
#define GPIOC_BASE 							0x33U
//	Port [D]
#define GPIOD_BASE 							0x30U


//	EXTI *******
#define EXTI_BASE 							0x54U


//	AFIO *******
#define AFIO_BASE 							0x50U



//===================================================================================================================



//-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*
//	Peripherals Registers
//-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*

//-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*
//	Peripheral Registers: GPIO *******
//-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*
typedef struct {
	volatile uint8_t	PINx;
	volatile uint8_t	DDRx;
	volatile uint8_t	PORTx;
} GPIO_TypeDef;



//-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*
//	Peripheral Registers: EXTI *******
//-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*
typedef struct {
	volatile uint8_t MCUCSR;
	volatile uint8_t MCUCR;
	uint32_t RESERVED0;
	volatile uint8_t GIFR;
	volatile uint8_t GICR;
} EXTI_TypeDef;

//-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*
//	Peripheral Registers: AFIO *******
//-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*
typedef struct {
	volatile uint8_t SFIOR;
} AFIO_TypeDef;



//===================================================================================================================



//-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*
//	Peripheral Instants:
//-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*

//	GPIO *******
#define GPIOA			((GPIO_TypeDef *)GPIOA_BASE)
#define GPIOB			((GPIO_TypeDef *)GPIOB_BASE)
#define GPIOC			((GPIO_TypeDef *)GPIOC_BASE)
#define GPIOD			((GPIO_TypeDef *)GPIOD_BASE)


//	EXTI *******
#define EXTI			((EXTI_TypeDef *)EXTI_BASE)


//	AFIO *******
#define AFIO			((AFIO_TypeDef *)AFIO_BASE)



//===================================================================================================================







#endif /* ATMEGA32_H_ */